BEGIN;

-- Clientes
CREATE TABLE clientes (
    id SERIAL CONSTRAINT pk_clientes PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    email VARCHAR(100) UNIQUE,
    data_cadastro DATE DEFAULT CURRENT_DATE
);

-- Produtos
CREATE TABLE produtos (
    id SERIAL CONSTRAINT pk_produtos PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    categoria VARCHAR(50),
    preco NUMERIC(10,2) NOT NULL,
    estoque INT DEFAULT 0
);

-- Vendedores
CREATE TABLE vendedores (
    id SERIAL CONSTRAINT pk_vendedores PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    departamento VARCHAR(50)
);

-- Pedidos
CREATE TABLE pedidos (
    id SERIAL CONSTRAINT pk_pedidos PRIMARY KEY,
    cliente_id INT NOT NULL,
    CONSTRAINT fk_cliente FOREIGN KEY (cliente_id) REFERENCES clientes(id) ON DELETE CASCADE,
    vendedor_id INT NOT NULL,
    CONSTRAINT fk_vendedor FOREIGN KEY (vendedor_id) REFERENCES vendedores(id) ON DELETE SET NULL,
    data_pedido TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    status VARCHAR(20) DEFAULT 'pendente'
);

-- Itens do Pedido
CREATE TABLE itens_pedido (
    id SERIAL CONSTRAINT pk_itens PRIMARY KEY,
    pedido_id INT NOT NULL,
    CONSTRAINT fk_pedido FOREIGN KEY (pedido_id) REFERENCES pedidos(id) ON DELETE CASCADE,
    produto_id INT NOT NULL,
    CONSTRAINT fk_produto FOREIGN KEY (produto_id) REFERENCES produtos(id) ON DELETE RESTRICT,
    quantidade INT NOT NULL CHECK (quantidade > 0),
    preco_unitario NUMERIC(10,2) NOT NULL
);

-- Pagamentos
CREATE TABLE pagamentos (
    id SERIAL CONSTRAINT pk_pagamentos PRIMARY KEY,
    pedido_id INT NOT NULL,
    CONSTRAINT fk_pagamento_pedido FOREIGN KEY (pedido_id) REFERENCES pedidos(id) ON DELETE CASCADE,
    data_pagamento DATE NOT NULL,
    valor_pago NUMERIC(10,2) NOT NULL CHECK (valor_pago >= 0),
    metodo_pagamento VARCHAR(50)
);





